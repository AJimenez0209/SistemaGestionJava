    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        //La idea es eliminar de  la lista siguiente, private List<String> lista = new ArrayList<String>();, quitarle elementos el que este seleccionado
        // En este get obtengo el numero del array donde esta el elemento seleccionado 0,1,2,etc...
        int indice = this.lstClientes.getSelectedIndex();
        Cliente cliente = lista.get(indice);
        ClienteDao dao = new ClienteDao();
        dao.eliminar(cliente.getId());

        actualizarLista();
        JOptionPane.showMessageDialog(rootPane, "Se eliminó correctamente");

    }//GEN-LAST:event_btnEliminarActionPerformed

