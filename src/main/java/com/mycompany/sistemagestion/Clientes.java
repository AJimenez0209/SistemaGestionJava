    private void actualizarLista() {
        ClienteDao dao = new ClienteDao();
        lista = dao.listar();
        DefaultListModel datosClientes = new DefaultListModel();

        //actualizar lista del diseño
        //acceder a la lista 
        //se instancia el modelo de DATOS
        //  Al ser de tipo diferente se tienen que recorrer esta lista private List<String> lista = new ArrayList<String>();
        //  se recorre la lista para obtener el tamaño que vaya teniendo e iterando por cada elemento que exista 
        for (int i = 0; i < lista.size(); i++) {
            //se guarda el recorrido en la variable nombre
            Cliente cliente = lista.get(i);
            datosClientes.addElement(cliente.getNombreCompleto());

        }

        this.lstClientes.setModel(datosClientes);

    }

