    private void btnCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarActionPerformed
        // TODO add your handling code here:

        //accediendo a la caja de texto y nos devuelve el texto escrito y se guarda en una variable
        String nombre = this.txtNombre.getText();
        String strAltura = this.txtAltura.getText();
        String strPeso = this.txtPeso.getText();
        
        //Conversion de texto a Double
        Double altura = Double.parseDouble(strAltura);
        Double peso = Double.parseDouble(strPeso);
        
        // Formula para poder calcular el indice de masa corporal utilizando la formula peso / altura al cuadrado 
        Double imc = peso / (altura * altura); // altura * altura es altura al cuadrado
        
        JOptionPane.showMessageDialog(rootPane, "Hola  " + nombre + ", tu indice de masa corporal es:  " + imc  );
        

       
    }//GEN-LAST:event_btnCargarActionPerformed

